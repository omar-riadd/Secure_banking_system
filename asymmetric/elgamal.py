# A 512-bit safe prime: p = 2q + 1 where both p and q are prime.
# "Safe primes" are preferred for ElGamal because the subgroup structure
# of Z_p* is simpler — the only subgroup of order > 2 has order q,
# which makes the DLP provably as hard as possible for this group size.


def _get_domain_params(bits: int = 512):
    """
    Return domain parameters (p, g) for ElGamal.

    DOMAIN PARAMETERS:
      p : a large prime — defines the group Z_p*
      g : a primitive root mod p — the group generator

    These parameters are PUBLIC — they can be shared openly.
    Security comes from the private key x, not from hiding p or g.

    NOTE ON KEY SIZES:
      128-bit keys are used in this demo for speed.
      Production systems use 2048+ bit primes (RSA-equivalent security).
      The algorithms and protocol structure are identical regardless of size.
    """
    if bits == 512:
        p = PRECOMPUTED_SAFE_PRIME_512[0]
        g = _find_generator(p)
        return p, g
    else:
        # Generate a fresh safe prime on-the-fly (slower for large bits)
        q = _generate_prime(bits - 1)
        p = 2 * q + 1
        while not _is_prime(p):
            q = _generate_prime(bits - 1)
            p = 2 * q + 1
        g = _find_generator(p)
        return p, g


# ═══════════════════════════════════════════════════════════════
#  KEY GENERATION
# ═══════════════════════════════════════════════════════════════

def generate_keypair(bits: int = 512) -> dict:
    """
    Generate an ElGamal key pair.

    THE MATHEMATICS:
      1. Choose domain parameters: large prime p, generator g
      2. Choose private key: x ← random integer in [2, p-2]
         x is chosen uniformly at random — its security relies on
         the adversary not being able to compute x from g^x mod p (DLP)
      3. Compute public key: y = g^x mod p
         This is the "one-way" computation — easy forward, hard to reverse

    KEY RELATIONSHIPS:
      Private key (x): kept SECRET. Used to decrypt and sign.
      Public key (p, g, y): shared OPENLY. Used to encrypt and verify.

    SESSION KEY PROTOCOL ROLE:
      During the handshake (Step 1), the SERVER'S public key (p, g, y)
      is sent to the client. The client uses y to encrypt the session key.
      Only the server — holding private key x — can decrypt it.

    DYNAMICITY NOTE:
      The session key itself (the 128-bit random integer m) is what
      changes on every handshake. The ElGamal keypair is LONG-TERM
      and remains the same for the server across all sessions. The
      ephemeral key k (chosen fresh per encryption) provides semantic
      security — same m encrypted twice gives different (c1, c2).
    """
    p, g = _get_domain_params(bits)
    x    = random.randint(2, p - 2)    # private key: random in [2, p-2]
    y    = _mod_exp(g, x, p)           # public key:  y = g^x mod p
    return {"p": p, "g": g, "x": x, "y": y}


def get_public_key(keypair: dict) -> dict:
    """
    Extract the public portion of a keypair (safe to share with anyone).
    Omits the private key x — this is the certificate's public key.
    """
    return {"p": keypair["p"], "g": keypair["g"], "y": keypair["y"]}


# ═══════════════════════════════════════════════════════════════
#  ENCRYPTION / DECRYPTION  (integer)
# ═══════════════════════════════════════════════════════════════

def elgamal_encrypt_int(m: int, pub: dict) -> tuple:
    """
    Encrypt an integer m using the recipient's ElGamal public key.

    MATHEMATICAL STEPS:

      Setup: public key is (p, g, y) where y = g^x mod p (x is secret)

      Step 1 — Choose ephemeral key k:
        k ← random integer in [2, p-2]
        k is used ONLY for this one encryption, then discarded.
        Using a fresh k for every encryption is what makes ElGamal
        semantically secure: encrypting the same message twice gives
        different ciphertexts (because k differs each time).
        ⚠ WARNING: reusing k for two different messages allows an
        attacker to compute the private key x — k MUST be unique.

      Step 2 — Compute c1 (the ephemeral public component):
        c1 = g^k mod p
        c1 allows the recipient to compute the shared secret (Step D below)
        without revealing k (because reversing g^k mod p requires solving DLP)

      Step 3 — Compute c2 (the masked message):
        c2 = m × y^k mod p
        y^k = (g^x)^k = g^(xk) mod p  — the "shared secret"
        c2 hides m behind multiplication by this shared secret.
        Without knowing x (to compute y^k), c2 reveals nothing about m.

      CIPHERTEXT: (c1, c2) — both are public to transmit

    SESSION KEY ESTABLISHMENT:
      In the banking handshake, m is the 128-bit session key integer.
      The client calls this function with the SERVER's public key.
      The resulting (c1, c2) is sent to the server over the network.
      Only the server can recover m — satisfying Step 4 of the protocol.

    HANDWRITTEN VERIFICATION (small numbers):
      p=23, g=5, x=6 → y = 5^6 mod 23 = 8
      m=10, k=3:
        c1 = 5^3 mod 23 = 125 mod 23 = 10
        c2 = 10 × 8^3 mod 23 = 10 × 512 mod 23 = 5120 mod 23 = 14
      Ciphertext: (10, 14)
    """
    p, g, y = pub["p"], pub["g"], pub["y"]

    # Step 1: fresh ephemeral key — MUST be unique per encryption
    k  = random.randint(2, p - 2)

    # Step 2: c1 = g^k mod p  (ephemeral public component)
    c1 = _mod_exp(g, k, p)

    # Step 3: c2 = m × y^k mod p  (message masked by shared secret)
    c2 = (m * _mod_exp(y, k, p)) % p

    return c1, c2


def elgamal_decrypt_int(c1: int, c2: int, priv: dict) -> int:
    """
    Decrypt an ElGamal ciphertext (c1, c2) using the private key.

    MATHEMATICAL STEPS:

      Given: ciphertext (c1, c2), private key x, prime p

      Step A — Compute the shared secret s:
        s = c1^x mod p
        Because c1 = g^k, we get: c1^x = (g^k)^x = g^(kx) mod p
        This equals y^k = (g^x)^k = g^(xk) mod p — the SAME value
        the encryptor used! Both parties compute g^(kx) independently:
          Encryptor: y^k = (g^x)^k   (using the public key y)
          Decryptor: c1^x = (g^k)^x  (using the private key x)
        This is the Diffie-Hellman shared secret mechanism.

      Step B — Compute the modular inverse of s:
        s_inv = s^(-1) mod p  (using Extended Euclidean Algorithm)

      Step C — Recover the original message:
        m = c2 × s_inv mod p
        Because c2 = m × y^k = m × s:
          c2 × s^(-1) = m × s × s^(-1) = m × 1 = m  ✓

    HANDWRITTEN VERIFICATION (continuing from encrypt example):
      c1=10, c2=14, x=6, p=23:
        s     = 10^6 mod 23 = 1000000 mod 23 = 6
        s_inv = 6^(-1) mod 23 = 4   (since 6×4 = 24 ≡ 1 mod 23)
        m     = 14 × 4 mod 23 = 56 mod 23 = 10  ✓  matches original
    """
    p, x = priv["p"], priv["x"]

    # Step A: shared secret s = c1^x mod p
    # This equals g^(kx) — the same value y^k computed by the encryptor
    s = _mod_exp(c1, x, p)

    # Step B: modular inverse of s
    s_inv = _mod_inverse(s, p)

    # Step C: recover m = c2 × s^(-1) mod p
    return (c2 * s_inv) % p


# ═══════════════════════════════════════════════════════════════
#  ENCRYPTION / DECRYPTION  (bytes — chunked)
# ═══════════════════════════════════════════════════════════════

def _bytes_to_int(b: bytes) -> int:
    """Convert bytes to a big-endian integer."""
    return int.from_bytes(b, "big")


def _int_to_bytes(n: int, length: int) -> bytes:
    """Convert an integer to big-endian bytes of fixed length."""
    return n.to_bytes(length, "big")


def elgamal_encrypt_bytes(plaintext: bytes, pub: dict) -> list:
    """
    Encrypt arbitrary bytes by splitting into chunks smaller than p.

    WHY CHUNKING?
      ElGamal operates on integers m where 0 ≤ m < p.
      For large plaintexts, we split into (p.bit_length()//8 - 1)-byte
      chunks, each small enough to be treated as an integer < p.
      Each chunk is encrypted independently, producing one (c1, c2) pair.

    EFFICIENCY NOTE:
      In practice, ElGamal is used ONLY for small data — specifically,
      to encrypt a symmetric key (e.g. a 16-byte session key).
      Bulk data is always encrypted with a symmetric cipher (XTEA/Twofish).
      This is the hybrid encryption model used in TLS, PGP, and our system.
    """
    p          = pub["p"]
    chunk_size = (p.bit_length() // 8) - 1   # largest chunk safely < p
    chunks     = []

    for i in range(0, len(plaintext), chunk_size):
        chunk  = plaintext[i : i + chunk_size]
        m      = _bytes_to_int(chunk)
        chunks.append(elgamal_encrypt_int(m, pub))

    return chunks


def elgamal_decrypt_bytes(cipherchunks: list, priv: dict, original_len: int) -> bytes:
    """
    Decrypt a list of (c1, c2) pairs and reassemble the original bytes.
    original_len is needed to correctly size the last (possibly short) chunk.
    """
    p          = priv["p"]
    chunk_size = (p.bit_length() // 8) - 1
    result     = b""

    for i, (c1, c2) in enumerate(cipherchunks):
        m = elgamal_decrypt_int(c1, c2, priv)
        # The last chunk may be shorter than chunk_size
        this_len = min(chunk_size, original_len - i * chunk_size)
        result  += _int_to_bytes(m, this_len)

    return result


# ═══════════════════════════════════════════════════════════════
#  DIGITAL SIGNATURES
# ═══════════════════════════════════════════════════════════════

def sign(message: bytes, keypair: dict) -> tuple:
    """
    Sign a message using the ElGamal signature scheme.

    THE MATHEMATICS:

      Setup: private key x, prime p, generator g, public key y = g^x mod p

      Step 1 — Hash the message:
        h = SHA-256(message) mod (p-1)
        Hashing binds the signature to the EXACT content of the message.
        Any modification to message changes h, invalidating the signature.
        The hash is reduced mod (p-1) to fit within the group order.

      Step 2 — Choose ephemeral key k:
        k ← random in [2, p-2],  gcd(k, p-1) = 1  (required for invertibility)
        k MUST be unique per signature — reusing k with two messages
        allows an attacker to compute x (the private key).

      Step 3 — Compute r (the commitment):
        r = g^k mod p
        r commits to k without revealing it.

      Step 4 — Compute s (the signature value):
        s = k^(-1) × (h - x×r) mod (p-1)
        Rearranging: k×s = h - x×r  (mod p-1)
                     h   = x×r + k×s (mod p-1)
        This is the equation verified during signature checking.

      SIGNATURE: (r, s)

    BANKING ROLE:
      Every transaction sent by a client is signed with their private key.
      The server verifies using the client's public key from their CA certificate.
      This ensures:
        1. AUTHENTICATION: only the key holder could produce this signature
        2. INTEGRITY: any modification to the transaction invalidates the sig
        3. NON-REPUDIATION: the client cannot deny sending the transaction
    """
    p, g, x = keypair["p"], keypair["g"], keypair["x"]
    p1       = p - 1   # group order is p-1 for prime p

    # Step 1: hash the message and reduce mod (p-1)
    h = int(hashlib.sha256(message).hexdigest(), 16) % p1

    while True:
        # Step 2: fresh ephemeral k coprime to (p-1)
        # gcd(k, p-1) = 1 ensures k is invertible mod (p-1)
        k = random.randint(2, p1 - 1)
        if gcd(k, p1) != 1:
            continue    # try again if not coprime

        # Step 3: r = g^k mod p  (commitment to k)
        r = _mod_exp(g, k, p)

        # Step 4: s = k^(-1) × (h - x×r) mod (p-1)
        k_inv = _mod_inverse(k, p1)
        s     = (k_inv * (h - x * r)) % p1

        if s != 0:      # s=0 is degenerate — retry
            return r, s


def verify(message: bytes, r: int, s: int, pub: dict) -> bool:
    """
    Verify an ElGamal signature (r, s) on a message.

    VERIFICATION EQUATION:
      A valid signature satisfies:  g^h ≡ y^r × r^s  (mod p)

    WHY THIS WORKS (mathematical derivation):
      From signing: h = x×r + k×s  (mod p-1)
      Therefore:    g^h = g^(x×r + k×s) = g^(xr) × g^(ks)  (mod p)
                        = (g^x)^r × (g^k)^s
                        = y^r × r^s  (mod p)  ← because y=g^x, r=g^k

      If any part of the message changes → h changes → equation fails.
      If (r, s) is forged → without x and k, cannot satisfy the equation.

    TAMPER DETECTION:
      If the transaction amount "5000" is changed to "50000" after signing:
        New hash h' = SHA-256(tampered) ≠ h
        g^h' ≠ y^r × r^s  (the equation fails)
        verify() returns False → server rejects the transaction
    """
    p, g, y = pub["p"], pub["g"], pub["y"]
    p1      = p - 1

    # Basic sanity check: r must be in range [1, p-1]
    if not (0 < r < p):
        return False

    # Compute hash of message mod (p-1)
    h   = int(hashlib.sha256(message).hexdigest(), 16) % p1

    # Left-hand side: g^h mod p
    lhs = _mod_exp(g, h, p)

    # Right-hand side: y^r × r^s mod p
    rhs = (_mod_exp(y, r, p) * _mod_exp(r, s, p)) % p

    # Valid signature iff both sides are equal
    return lhs == rhs


