"""
ElGamal Public Key Cryptosystem
=================================
Implemented entirely from scratch using only Python integer arithmetic.
No encryption libraries used. Only: pow(), //, %, ^, basic arithmetic.

ACADEMIC REFERENCE
  ElGamal, T. (1985).
  "A Public Key Cryptosystem and a Signature Scheme Based on Discrete Logarithms."
  IEEE Transactions on Information Theory, 31(4), 469–472.

ALGORITHM CLASS
  Asymmetric (public key) cryptosystem — based on the Discrete Logarithm Problem

BANKING USE CASE  (two distinct roles)
  Role 1 — SESSION KEY ESTABLISHMENT (Steps 3–4 of the handshake protocol):
    Client encrypts a random 128-bit session key using the server's
    ElGamal public key. Server decrypts it using its private key.
    This is how two parties establish a shared secret without
    ever sending that secret in plaintext.

  Role 2 — DIGITAL SIGNATURES (every transaction):
    Client signs every transaction using its ElGamal private key.
    Server verifies using the client's public key (from CA certificate).
    Provides authentication (proves who sent it) and integrity
    (proves the message was not tampered with).

SESSION KEY PROTOCOL CONNECTION
  ElGamal encryption IS the mechanism of Step 3–4 of the session key
  establishment protocol. The "session key" is the plaintext m —
  a 128-bit random integer generated by the client. ElGamal encrypts
  it as (c1, c2) and sends it to the server. The server decrypts and
  both parties now share the same secret. XTEA then uses this secret
  for all subsequent message encryption.

WHY ELGAMAL INSTEAD OF RSA?
  - Not RSA or ECC (meets the "other than explained in class" requirement)
  - Based on a different hard problem (DLP vs integer factorisation)
  - Randomised encryption: same message + same key → different ciphertext
    (due to ephemeral k) — provides semantic security
  - Supports both encryption AND signatures from the same key structure
  - Simple to implement from scratch: only modular exponentiation needed

SECURITY BASIS — DISCRETE LOGARITHM PROBLEM (DLP)
  Given: a large prime p, a generator g, and y = g^x mod p
  Find:  x  (the discrete logarithm of y base g modulo p)

  The EASY direction: compute y = g^x mod p  (fast with pow())
  The HARD direction: recover x from y       (no efficient algorithm known)

  The best known classical algorithm (GNFS) takes sub-exponential time —
  still astronomical for 512+ bit primes. This asymmetry is the
  foundation of ElGamal's security.
"""

