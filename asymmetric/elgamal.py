    print(f"  Public key  : y = g^x mod p = {g}^{x} mod {p} = {y}")
    print(f"\n  MESSAGE     : m = {m}")
    print(f"  EPHEMERAL   : k = {k}  (fresh random per encryption)")

    # ── Encryption ────────────────────────────────────────────
    c1 = _mod_exp(g, k, p)
    c2 = (m * _mod_exp(y, k, p)) % p

    print(f"\n  ── ENCRYPTION ────────────────────────────────────")
    print(f"  c1 = g^k mod p")
    print(f"     = {g}^{k} mod {p}")
    print(f"     = {g**k} mod {p}")
    print(f"     = {c1}")
    print(f"  c2 = m × y^k mod p")
    print(f"     = {m} × {y}^{k} mod {p}")
    print(f"     = {m} × {y**k} mod {p}")
    print(f"     = {m * (y**k)} mod {p}")
    print(f"     = {c2}")
    print(f"  Ciphertext  : (c1={c1}, c2={c2})")

    # ── Decryption ────────────────────────────────────────────
    s     = _mod_exp(c1, x, p)
    s_inv = _mod_inverse(s, p)
    m_rec = (c2 * s_inv) % p

    print(f"\n  ── DECRYPTION ────────────────────────────────────")
    print(f"  s     = c1^x mod p = {c1}^{x} mod {p} = {s}")
    print(f"  s_inv = {s}^(-1) mod {p} = {s_inv}")
    print(f"          [verify: {s} × {s_inv} = {s*s_inv} ≡ {(s*s_inv)%p} mod {p}  ✓]")
    print(f"  m     = c2 × s_inv mod p")
    print(f"        = {c2} × {s_inv} mod {p}")
    print(f"        = {c2 * s_inv} mod {p}")
    print(f"        = {m_rec}")
    print(f"\n  Match original message ({m})? {'✅ YES' if m_rec == m else '❌ NO'}")

    # ── Signature mini-demo ───────────────────────────────────
    print(f"\n  ── DIGITAL SIGNATURE (concept) ───────────────────")
    print(f"  To sign message m={m}:")
    print(f"    h = hash(m) mod (p-1)  [SHA-256 in real system]")
    print(f"    r = g^k mod p          [commitment to ephemeral k]")
    print(f"    s = k^(-1)×(h - x×r) mod (p-1)  [signature value]")
    print(f"  To verify: check  g^h ≡ y^r × r^s  (mod p)")
    print(f"  Any change to m → h changes → equation fails → REJECTED")
    print("=" * 60)


